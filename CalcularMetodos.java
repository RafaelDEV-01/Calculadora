package Metodos;

public class CalcularMetodos {
	
	double somar (double a, double b) {
		return a + b;
	}
	double somar (double a, double b, double c) {
		return a + b + c;
	}
	double mult (double a, double b) {
		return a * b;
	}
	double div (double a, double b) {
		return a / b;
	}
	double sub (double a, double b) {
		return a - b;
	}
	
	
}
